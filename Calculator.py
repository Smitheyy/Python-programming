while True:

    def calculate_again():

        calculate = input("Do you want to use the calculator again?(type Y or N): ").upper()
        print("-------------------------------------------------------------------------------------------------------")

        if calculate == "Y":
            return True

        elif calculate == "N":
            print("---------------------------------------------------------------------------------------------------")
            print("Thank you for using my calculator, please have a nice day! :)")
            print("---------------------------------------------------------------------------------------------------")
            exit()

        else:
            print("Please type only one of the two options (Y or N)")
            print("---------------------------------------------------------------------------------------------------")
            calculate_again()


    add = lambda x, y: x + y
    subtract = lambda x, y: x - y
    multiply = lambda x, y: x * y


    def divide(x, y):
        if y == 0:
            print("You can't divide by 0...idiot!")
            print("---------------------------------------------------------------------------------------------------")
            calculate_again()
        return x / y


    try:

        number_1 = float(input("Choose the first number: "))
        print("-------------------------------------------------------------------------------------------------------")
        number_2 = float(input("Choose the second number: "))
        print("-------------------------------------------------------------------------------------------------------")

    except ValueError as n:
        print("-------------------------------------------------------------------------------------------------------")
        print("Enter numbers only!")
        print("-------------------------------------------------------------------------------------------------------")
        print(n)
        print("-------------------------------------------------------------------------------------------------------")
        calculate_again()

    print("Choose an action: ")
    print("1.sum_up")
    print("2.subtract")
    print("3.multiply")
    print("4.divide")
    print("-----------------------------------------------------------------------------------------------------------")

    try:
        choice = int(input("Which operation would you like to pick? (choose 1, 2, 3 or 4): "))

        if choice == 1:
            print("---------------------------------------------------------------------------------------------------")
            print(number_1, "+", number_2, "=", add(number_1, number_2))
            print("---------------------------------------------------------------------------------------------------")

        elif choice == 2:
            print("---------------------------------------------------------------------------------------------------")
            print(number_1, "-", number_2, "=", subtract(number_1, number_2))
            print("---------------------------------------------------------------------------------------------------")

        elif choice == 3:
            print("---------------------------------------------------------------------------------------------------")
            print(number_1, "*", number_2, "=", multiply(number_1, number_2))
            print("---------------------------------------------------------------------------------------------------")

        elif choice == 4:
            print("---------------------------------------------------------------------------------------------------")
            print(number_1, ":", number_2, "=", divide(number_1, number_2))
            print("---------------------------------------------------------------------------------------------------")

    except ValueError as p:
        print("Enter numbers only!")
        print("-------------------------------------------------------------------------------------------------------")
        print(p)
        print("-------------------------------------------------------------------------------------------------------")
        calculate_again()

    calculate_again()
